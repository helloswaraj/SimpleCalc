operand1 = float(input("Enter the first number: "))
operand2 = float(input("Enter the second number: "))
operator = input("Enter operator (+, -, *, /): ")
if operator == '+':
    result = operand1 + operand2
    print("Result:", result)
if operator == '-':
    result = operand1 - operand2
    print("Result:", result)
